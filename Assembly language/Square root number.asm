.MODEL SMALL
.STACK 100
.DATA
M1 DW 0009H
M2 DB 0AH,0DH,"NO ROOT"
.CODE
START:MOV AX,@DATA
MOV DS,AX
MOV SI,OFFSET M1
MOV DX,[SI]
CMP DX,0001H
JZ EXIT1

MOV AX,DX
MOV CL,0002H
DIV CL
MOV BL,AL
MOV BH,00H
MOV CX,0001H
L1:MOV AH,CL
MOV AL,CL
MUL AH
CMP AX,DX
JZ SKIP
CMP CX,BX
JNC EXIT1
INC CX
JMP L1

EXIT1:LEA DX,M2
MOV AH,09H
INT 21H
JMP L2
SKIP:MOV DX,CX
ADD DX,0030H
MOV AH,02H
INT 21H
L2:NOP
.EXIT
END START